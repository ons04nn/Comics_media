function sectionText(topic, index, heading, title, subject) {
  const fn = SECTION_TEXT[topic] ?? SECTION_TEXT.comics;
  return fn(index, heading, title, subject);
}

function buildIntro(title, subject, topic) {
  const intros = {
    comics: `«${title}» — развёрнутый материал о комиксах и графических романах. ${subject} здесь — не просто заголовок карточки, а повод пройти путь от первых выпусков до современного статуса. Ниже — десять блоков: история, стиль, герои, темы и то, почему эта работа до сих пор важна для читателей RetroNova.`,
    manga: `«${title}» — подробный разбор японской visual culture. ${subject} рассматривается через сюжет, рисунок, адаптации и культурный контекст. Десять секций ниже помогут разобраться в материале так же глубоко, как в оригинальном editorial RetroNova.`,
    production: `«${title}» — практический гид для авторов комиксов. Материал ведёт от идеи к готовой странице и объясняет процесс так, чтобы ${subject.toLowerCase()} можно было применить сразу. Десять блоков — десять шагов к собственной истории.`,
    history: `«${title}» — исторический разбор ключевого момента в комикс-индустрии. ${subject} становится линзой, через которую видны decades изменений. Ниже — десять секций от контекста эпохи до современного наследия.`,
    publisher: `«${title}» — история издательства и его роли на рынке. ${subject} — пример того, как business и creativity переплетаются. Десять блоков раскрывают путь от основания до влияния на industry сегодня.`,
    region: `«${title}» — материал о regional comics culture. ${subject} показывает, как geography и tradition создают unique visual languages. Десять секций — от истоков до современного rediscovery.`,
  };
  return intros[topic] ?? intros.comics;
}

function buildItems(title, subject, topic) {
  const headings = SECTION_HEADINGS[topic] ?? SECTION_HEADINGS.comics;

  return headings.map((heading, index) => ({
    align: index % 2 === 0 ? 'left' : 'right',
    image: ARTICLE_LIST_IMAGES[index],
    html: `<b>${index + 1}. ${heading}</b><br>${sectionText(topic, index, heading, title, subject)}`,
  }));
}

export function generateArticleContent({ slug, titleLines, alt, topic = 'comics' }) {
  const title = titleText(titleLines);
  const subject = alt && alt !== title ? alt : title;

  return {
    intro: buildIntro(title, subject, topic),
    items: buildItems(title, subject, topic),
  };
}
